
/**
 * Quick-Find Data Structure
 */


public class QuickFind {
    private int[] id;

    public QuickFind(int n) {
        this.id = new int[n];
        // Set ID of each object to itself
        for (int i = 0; i < n; i++) {
            id[i] = i;
        }
    }

    public boolean connected(int p, int q) {
        return id[p] == id[q];
    }

    public void union(int p, int q) {
        int pid = id[p];
        int qid = id[q];
        for (int i = 0; i < id.length; i++) {
            if (id[i] == pid) {
                id[i] = qid;
            }
        }
    }
}